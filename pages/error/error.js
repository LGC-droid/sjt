// pages/error/error.js
import { previousPageClass } from '../../utils/util.js'

let isClickReload = false

Page({

  /**
   * 页面的初始数据
   */
  data: {

  },

  /**
   * 生命周期函数--监听页面加载
   */
  onLoad: function (options) {
    isClickReload = false
  },

  /**
   * 生命周期函数--监听页面初次渲染完成
   */
  onReady: function () {

  },

  /**
   * 生命周期函数--监听页面显示
   */
  onShow: function () {

  },

  /**
   * 生命周期函数--监听页面隐藏
   */
  onHide: function () {

  },

  /**
   * 生命周期函数--监听页面卸载
   */
  onUnload: function () {
    if (!isClickReload) {
      this.callbackParams()
    }
  },

  /**
   * 页面相关事件处理函数--监听用户下拉动作
   */
  onPullDownRefresh: function () {

  },

  /**
   * 页面上拉触底事件的处理函数
   */
  onReachBottom: function () {

  },

  /**
   * 用户点击右上角分享
   */
  onShareAppMessage: function () {

  },
  /**
   * 点击事件
   */
  clickReload: function (e) {
    isClickReload = true
    wx.navigateBack()
    this.callbackParams()
  },
  // 点击返回，参数回传
  callbackParams: function () {
    let preOptions = previousPageClass().options
    previousPageClass().onLoad(preOptions)
  }
})